namespace Aura.Framework.Connectivity.Server.DatabaseEngine.Enumerators
{
    /// <summary>
    /// Represents an enumerator to specify a gender.
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// Represents an unknown gender.
        /// </summary>
        Unknown,

        /// <summary>
        /// Represents a male gender.
        /// </summary>
        Male,

        /// <summary>
        /// Represents a female gender.
        /// </summary>
        Female,

        /// <summary>
        /// Represents an unspecified gender.
        /// </summary>
        Other,
    }
}