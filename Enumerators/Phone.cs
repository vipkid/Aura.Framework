namespace Aura.Framework.Enumerators
{
    /// <summary>
    /// Represents an enumerator to specify the phone-number type of a user.
    /// </summary>
    internal enum Phone
    {
        /// <summary>
        /// Represents an unknown phone-number type.
        /// </summary>
        Unknown,

        /// <summary>
        /// Represents a mobile phone-number type.
        /// </summary>
        Mobile,

        /// <summary>
        /// Represents a home phone-number type.
        /// </summary>
        Home,

        /// <summary>
        /// Represents a work phone-number type.
        /// </summary>
        Work,

        /// <summary>
        /// Represents an unknown phone-number type.
        /// </summary>
        Other
    }
}