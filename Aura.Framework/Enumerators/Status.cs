namespace Aura.Framework.Enumerators
{
    /// <summary>
    /// Represents an enumerator to specify the status of a user.
    /// </summary>
    public enum Status
    {
        /// <summary>
        /// Represents an unknown status.
        /// </summary>
        Unknown,

        /// <summary>
        /// Represents the offline status.
        /// </summary>
        Offline,

        /// <summary>
        /// Represents the online status.
        /// </summary>
        Online,

        /// <summary>
        /// Represents the away status.
        /// </summary>
        Away,

        /// <summary>
        /// Represents the busy status.
        /// </summary>
        Busy
    }
}