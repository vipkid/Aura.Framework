namespace Aura.Framework.Audio.Player.Enumerators
{
    /// <summary>
    /// Represents an enumerator to specify a media key like the play, stop, forward and backward buttons. Used for the keyboard hook.
    /// </summary>
    public enum MediaKeys
    {
        /// <summary>
        /// Represents the play/pause media key.
        /// </summary>
        PausePlay,

        /// <summary>
        /// Represents the stop media key.
        /// </summary>
        Stop,

        /// <summary>
        /// Represents the forward media key.
        /// </summary>
        Forward,

        /// <summary>
        /// Represents the backward media key.
        /// </summary>
        Backward
    }
}